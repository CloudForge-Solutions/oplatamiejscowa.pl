Metodologia przygotowania rankingu

Informacje na temat oferty większości operatorów zostały uzyskane przez nas w drodze bezpośredniego kontaktu z przedstawicielami tych firm. W pozostałych przypadkach posiłkowaliśmy się informacjami zawartymi na stronach internetowych operatorów.

Im bardziej zagłębiamy się w oferty bramek płatności, tym do głowy przychodzi nam więcej pytań, które chcemy zadać, by rozwiać wszelkie wątpliwości związane z tematem, więc na pewno nie powiedzieliśmy ostatniego słowa. Także częstuj się, tym co już jest, a wkrótce spodziewaj się kolejnych wersji rankingu rozbudowanych o więcej informacji i szczegółów! Jeśli masz jakieś uwagi lub pomysły, co jeszcze powinno się znaleźć w tym rankingu, koniecznie daj znać na maila: rankingi@convertis.pl